package tuan2;

// In các số nguyên dương nhỏ hơn 11, dùng for
public class BT6 {
    public static void main(String[] args) {
        System.out.println("Các số nguyên dương nhỏ hơn 11:");
        
        for (int tgb = 1; tgb < 11; tgb++) {
            System.out.print(tgb + " ");
        }
    }
}
